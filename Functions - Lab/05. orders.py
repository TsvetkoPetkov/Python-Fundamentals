def orders_sum(product: str, quantity: int):
    if product == "coffee":
        return (f"{quantity * 1.5:.2f}")
    if product == "water":
        return (f"{quantity:.2f}")
    if product == "coke":
        return (f"{quantity * 1.4:.2f}")
    if product == "snacks":
        return (f"{quantity * 2:.2f}")

product = input()
quantity = int(input())

print(orders_sum(product, quantity))
